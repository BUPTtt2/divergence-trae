/**
 * 根据 ID 取智囊
 */
export function getAgentById(id) {
  if (!id) return null;
  if (id === MASTER_AGENT.id) return MASTER_AGENT;
  return AGENTS.find((a) => a.id === id) || null;
}

/**
 * 取所有智囊 ID
 */
export function getAllAgentIds() {
  return AGENTS.map((a) => a.id);
}
